// Función para cargar las comunas según la región seleccionada
function cargarComunas() {
    const regionSelect = document.getElementById('region');
    const comunaSelect = document.getElementById('comuna');
    const regionSeleccionada = regionSelect.value;
    comunaSelect.innerHTML = '<option value="">Seleccione una comuna</option>'; // Reiniciar opciones
    if (regionSeleccionada && regionesComunas[regionSeleccionada]) {

        regionesComunas[regionSeleccionada].forEach(comuna => {
            const option = document.createElement('option');
            option.value = comuna;
            option.textContent = comuna;
            comunaSelect.appendChild(option);
        });
    }           
}